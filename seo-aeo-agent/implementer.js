// ─── IMAGE LAZY LOADING FIX ──────────────────────────────────────────────────
// Adds loading="lazy" to images that are missing it.
// Skips the LCP hero image (first image or images with data-hero attribute).

function fixImageLazyLoading(html) {
  let changed = false;
  let firstImg = true;

  const result = html.replace(/<img(\s[^>]*)>/gi, (match, attrs) => {
    // Skip hero image (first img on page) and already-lazy images
    if (firstImg || /loading=["']lazy["']/i.test(attrs) || /data-hero/i.test(attrs)) {
      firstImg = false;
      return match;
    }
    firstImg = false;
    changed = true;
    return `<img${attrs} loading="lazy">`;
  });

  return { html: result, changed };
}

