// ─── MAIN IMPLEMENT FUNCTION ──────────────────────────────────────────────────

function implement(htmlFiles, auditResults, cfg, publicDir) {
  const results = {
    pages: {},
    infrastructure: {},
    totalFixes: 0,
  };

  // Fix each HTML page
  for (const { name, path: filePath } of htmlFiles) {
    const auditResult = auditResults.pages[name] || { checks: {}, issues: [] };
    const pageResult = implementPage(filePath, name, auditResult);
    results.pages[name] = pageResult;
    results.totalFixes += pageResult.fixes.length;
  }

  // Generate infrastructure files
  results.infrastructure.robots = generateRobotsTxt(publicDir);
  results.infrastructure.sitemap = generateSitemapXml(publicDir);
  results.infrastructure.llms = generateLlmsTxt(publicDir);

  // Generate 404 page
  const result404 = generate404Page(publicDir);
  results.infrastructure.page404 = result404;
  if (result404.created) results.totalFixes++;

  // Create images directory with reminder
  createImagesReadme(publicDir);

  results.totalFixes += 3; // robots, sitemap, llms
  return results;
}

module.exports = { implement, implementPage, generateRobotsTxt, generateSitemapXml, generateLlmsTxt, generate404Page };
