/**
 * SEO + AEO Agent — Implementer
 *
 * Reads the audit results and automatically applies fixes to HTML files.
 * All modifications are IDEMPOTENT — safe to run multiple times.
 * The agent checks for existing implementations before injecting anything.
 *
 * What it does per page:
 *   1. Injects JSON-LD structured data schemas
 *   2. Adds/fixes canonical URL tag
 *   3. Adds og:image, og:url meta tags
 *   4. Adds Twitter Card meta block
 *   5. Adds lang attribute to <html> if missing
 *   6. Improves title/description if suboptimal
 *
 * What it creates at the site level:
 *   7.  robots.txt
 *   8.  sitemap.xml
 *   9.  llms.txt (updates existing)
 *   10. /images/ placeholder folder note
 */

'use strict';

const fs = require('fs');
const path = require('path');
const config = require('./config');
const schemas = require('./schemas');

const { site, pages } = config;

// ─── HELPERS ──────────────────────────────────────────────────────────────────

function hasPattern(html, pattern) {
  return pattern.test(html);
}

function injectBeforeClosingHead(html, block) {
  return html.replace(/(<\/head>)/i, `${block}\n$1`);
}

function injectAfterOpenBody(html, block) {
  return html.replace(/(<body[^>]*>)/i, `$1\n${block}`);
}

// ─── PAGE-LEVEL FIXES ─────────────────────────────────────────────────────────

function fixHtmlLang(html) {
  // Add lang="en" to <html> if missing
  if (!/lang=["']/.test(html)) {
    return html.replace(/<html([^>]*)>/i, '<html$1 lang="en">');
  }
  return html;
}

function fixCanonical(html, pageUrl) {
  if (/rel=["']canonical["']/.test(html)) return html;
  const canonicalTag = `  <link rel="canonical" href="${site.url}${pageUrl}">`;
  return injectBeforeClosingHead(html, canonicalTag);
}

function fixOgUrl(html, pageUrl) {
  if (/property=["']og:url["']/.test(html)) return html;
  const tag = `  <meta property="og:url" content="${site.url}${pageUrl}">`;
  return injectBeforeClosingHead(html, tag);
}

function fixOgTitle(html, pageName) {
  if (/property=["']og:title["']/.test(html)) return html;
  const pageCfg = pages[pageName] || {};
  const title = pageCfg.title || site.name;
  const tag = `  <meta property="og:title" content="${title}">`;
  return injectBeforeClosingHead(html, tag);
}

function fixOgDescription(html, pageName) {
  if (/property=["']og:description["']/.test(html)) return html;
  const pageCfg = pages[pageName] || {};
  const desc = pageCfg.description || site.description;
  const tag = `  <meta property="og:description" content="${desc}">`;
  return injectBeforeClosingHead(html, tag);
}

function fixOgType(html, pageName) {
  if (/property=["']og:type["']/.test(html)) return html;
  const pageCfg = pages[pageName] || {};
  const type = (pageCfg.type === 'Article' || pageCfg.type === 'BlogPosting') ? 'article' : 'website';
  const tag = `  <meta property="og:type" content="${type}">`;
  return injectBeforeClosingHead(html, tag);
}

function fixOgImage(html, pageName) {
  if (/property=["']og:image["']/.test(html)) return html;
  const pageCfg = pages[pageName] || {};
  const imgPath = pageCfg.ogImage || '/images/og-default.jpg';
  const tag = `  <meta property="og:image" content="${site.url}${imgPath}">
  <meta property="og:image:width" content="1200">
  <meta property="og:image:height" content="630">
  <meta property="og:image:alt" content="${site.tagline}">`;
  return injectBeforeClosingHead(html, tag);
}

function fixTwitterCard(html, pageName) {
  if (/name=["']twitter:card["']/.test(html)) return html;

  const pageCfg = pages[pageName] || {};
  const title = pageCfg.title || site.name;
  const desc = pageCfg.description || site.description;
  const imgPath = pageCfg.ogImage || '/images/og-default.jpg';

  const block = `  <!-- Twitter / X Card -->
  <meta name="twitter:card" content="summary_large_image">
  <meta name="twitter:site" content="${site.twitter}">
  <meta name="twitter:creator" content="${site.twitter}">
  <meta name="twitter:title" content="${title}">
  <meta name="twitter:description" content="${desc}">
  <meta name="twitter:image" content="${site.url}${imgPath}">`;

  return injectBeforeClosingHead(html, block);
}

function fixStructuredData(html, pageName) {
  const schemaBlock = schemas.buildSchemasForPage(pageName);
  if (!schemaBlock) return html;

  // Remove existing auto-generated schemas
  let cleanHtml = html.replace(/[\s]*<!-- SEO \+ AEO Structured Data — Auto-generated by seo-aeo-agent -->/gi, '');
  cleanHtml = cleanHtml.replace(/<script type="application\/ld\+json" data-schema-id="[^"]*">[\s\S]*?<\/script>/gi, '');

  const comment = `  <!-- SEO + AEO Structured Data — Auto-generated by seo-aeo-agent -->`;
  return injectBeforeClosingHead(cleanHtml, comment + '\n' + schemaBlock + '\n');
}

function fixTitle(html, pageName) {
  const pageCfg = pages[pageName];
  if (!pageCfg) return html;

  const currentTitle = html.match(/<title[^>]*>([\s\S]*?)<\/title>/i);
  if (!currentTitle) return html;

  const current = currentTitle[1].trim();
  const optimal = pageCfg.title;

  // Only replace if the optimal title is different and better
  if (current !== optimal && optimal) {
    return html.replace(/<title[^>]*>[\s\S]*?<\/title>/i, `<title>${optimal}</title>`);
  }
  return html;
}

function fixDescription(html, pageName) {
  const pageCfg = pages[pageName];
  if (!pageCfg) return html;

  const optimal = pageCfg.description;
  if (!optimal) return html;

  // Replace existing description
  const hasDesc = /<meta\s[^>]*name=["']description["'][^>]*>/i.test(html);
  if (hasDesc) {
    // Update content attribute of existing description
    return html.replace(
      /<meta(\s[^>]*)name=["']description["'](\s[^>]*)content=["'][^"']*["']([^>]*)>/i,
      `<meta$1name="description"$2content="${optimal}"$3>`
    ).replace(
      /<meta(\s[^>]*)content=["'][^"']*["'](\s[^>]*)name=["']description["']([^>]*)>/i,
      `<meta$1content="${optimal}"$2name="description"$3>`
    );
  }

  // Add missing description
  const tag = `  <meta name="description" content="${optimal}">`;
  return injectBeforeClosingHead(html, tag);
}

// ─── APPLY ALL PAGE FIXES ────────────────────────────────────────────────────

function implementPage(pagePath, pageName, auditResult) {
  const fixes = [];
  let html = fs.readFileSync(pagePath, 'utf8');
  const pageCfg = pages[pageName] || {};
  const pageUrl = pageCfg.url || `/${pageName.replace('.html', '')}`;

  const originalHtml = html;

  // 1. HTML lang attribute
  if (auditResult.checks.htmlLang && auditResult.checks.htmlLang.issues.length > 0) {
    html = fixHtmlLang(html);
    if (html !== originalHtml) fixes.push('Added lang="en" to <html>');
  }

  // 2. Title
  html = fixTitle(html, pageName);

  // 3. Meta description
  html = fixDescription(html, pageName);

  // 4. Canonical URL
  const prev = html;
  html = fixCanonical(html, pageUrl === '/index' ? '/' : pageUrl);
  if (html !== prev) fixes.push('Added canonical URL tag');

  // 5. OG tags (URL, Title, Description, Type)
  const prev2 = html;
  html = fixOgUrl(html, pageUrl === '/index' ? '/' : pageUrl);
  html = fixOgTitle(html, pageName);
  html = fixOgDescription(html, pageName);
  html = fixOgType(html, pageName);
  if (html !== prev2) fixes.push('Added missing og:url, og:title, og:description, or og:type meta tags');

  // 6. OG Image
  const prev3 = html;
  html = fixOgImage(html, pageName);
  if (html !== prev3) fixes.push('Added og:image meta tags');

  // 7. Twitter Card
  const prev4 = html;
  html = fixTwitterCard(html, pageName);
  if (html !== prev4) fixes.push('Added Twitter Card meta block');

  // 8. Structured Data (JSON-LD)
  const prev5 = html;
  html = fixStructuredData(html, pageName);
  if (html !== prev5) fixes.push('Injected JSON-LD structured data schemas (AEO + SEO)');

  // 9. Image lazy loading
  const lazyResult = fixImageLazyLoading(html);
  if (lazyResult.changed) {
    html = lazyResult.html;
    fixes.push('Added loading="lazy" to below-fold images');
  }

  // Write back if changed
  if (html !== originalHtml) {
    fs.writeFileSync(pagePath, html, 'utf8');
  }

  return { pageName, fixes, changed: html !== originalHtml };
}

