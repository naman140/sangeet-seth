// ─── SITEMAP.XML ──────────────────────────────────────────────────────────────

function generateSitemapXml(publicDir) {
  const sitemapPath = path.join(publicDir, 'sitemap.xml');
  const existing = fs.existsSync(sitemapPath);
  const today = new Date().toISOString().split('T')[0];

  const urlEntries = Object.entries(pages).map(([pageName, pageCfg]) => {
    const urlPath = pageCfg.url === '/' ? '' : pageCfg.url;
    return `  <url>
    <loc>${site.url}${urlPath}</loc>
    <lastmod>${today}</lastmod>
    <changefreq>${pageCfg.changefreq || 'monthly'}</changefreq>
    <priority>${pageCfg.priority || 0.8}</priority>
  </url>`;
  }).join('\n');

  const content = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"
        xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
        xsi:schemaLocation="http://www.sitemaps.org/schemas/sitemap/0.9
        http://www.sitemaps.org/schemas/sitemap/0.9/sitemap.xsd">

${urlEntries}

</urlset>
`;

  fs.writeFileSync(sitemapPath, content, 'utf8');
  return { path: sitemapPath, created: !existing, updated: existing };
}

